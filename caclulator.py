n1=int(input("enter first number="))
op=input("enter operand number    ")
n2=int(input("enter second number="))
if op=="+":
     print(n1+n2)
elif op=="-":
     print(n1-n2)
elif op=="*":
     print(n1*n2)
elif op=="/":
     print(n1/n2)
else:
     print("this is not a valid input")
     

              
